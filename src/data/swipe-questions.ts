/** Get questions grouped by dimension code */
export function getQuestionsByDimension(): Record<string, SwipeQuestionData[]> {
  return SWIPE_QUESTIONS.reduce<Record<string, SwipeQuestionData[]>>((acc, q) => {
    if (!acc[q.category]) acc[q.category] = []
    acc[q.category].push(q)
    return acc
  }, {})
}

/** Get questions filtered by type */
export function getQuestionsByType(type: string): SwipeQuestionData[] {
  return SWIPE_QUESTIONS.filter(q => q.type === type)
}


